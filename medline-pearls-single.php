                    <div class="single-subsection">
                        <div class="uppercase-subtitle">Diseño</div>
                        <p>Ensayo clínico, aleatorizado, doble Ciego, de grupos paralelos, y controlado con Placebo.</p>
                    </div>
                    
                    <div class="single-subsection">
                        <div class="uppercase-subtitle">Contexto</div>
                        <p>Este estudio fue realizado en la división de Hematología y Oncología, del Departamento de Pediatría en colaboración con el Departamento de Cardiología y Bioestadística del Instituto de Ciencias Médicas “Banaras Hindu University” en Varanasi, India y fue realizado entre agosto del 2014 hasta septiembre 2016 durante un periodo de 26 meses.</p>
                    </div>
                    
                    <div class="single-subsection">
                        <div class="uppercase-subtitle">Pacientes</div>
                        <p>Se incluyeron niños entre 2 a 16 años de edad, que recibieron antraciclinas como parte del Protocolo de quimioterapia, y que tenían una dosis acumulativa proyectada mayor o igual a 200 mg/m2. </p>
                        <p>Se excluyeron los pacientes con enfermedad recaída, con una FEVI menor al 50% o una fracción de acortamiento menor al 25%, con patología cardíaca preexistente, patología renal significativa (definida por una Tasa de filtrado glomerular estimada menor a 30 mL/kg/min), o enfermedad hepática significativa.</p>
                    </div>
                    
                    <div class="single-subsection">
                        <div class="uppercase-subtitle">Intervención</div>
                        <p>Los pacientes fueron distribuidos al azar en igual proporción entre recibir enalapril a una dosis de 0,1 mg/kg una vez al día desde el primer día de la quimioterapia por un periodo de 6 meses versus Placebo en el grupo de Control.</p>
                    </div>
                    
                    <div class="single-subsection">
                        <div class="uppercase-subtitle">Medidas de evolución</div>
                        <p>La medida primaria de valoración fue el descenso en la Incidencia de cardiotoxicidad (definida por un descenso mayor o igual al 20% en la FEVI desde el basal hasta los 6 meses). </p>
                        <p>Las medidas secundarias de valoración incluyeron la determinación de los niveles de biomarcadores cardíacos (previamente mencionados) a los 6 meses, y el desarrollo de insuficiencia cardíaca o arritmias durante este periodo.</p>
                    </div>
                    
                    <div class="single-subsection">
                        <div class="uppercase-subtitle">Principales resultados</div>
                        <p>-Se incluyeron en total 84 pacientes, de los cuales 44 fueron asignados al grupo con Intervención y 40 al grupo de Control con Placebo. </p>
                        <p>-La Mediana de edad de los pacientes fue 8,85 ± 3,15 y 8,77 ± 2,86 años en los dos grupos. </p>
                        <p>-La mayoría de los pacientes fueron de sexo masculino. </p>
                        <p>-Aproximadamente la mitad tenían diagnóstico de leucemia linfoblástica aguda y la otra mitad linfoma de Hodgkin. </p>
                        <p>-La dosis acumulativa de antraciclinas fue 272,73 ± 78,62 mg/m2 en el grupo con Intervención y 263,64 ± 80,90 mg/m2 en el grupo de Control (P=0,79). </p>
                        <p>-Ningún paciente en el grupo con enalapril presentó un descenso mayor o igual al 20% en la FEVI, mientras que 3 pacientes en el grupo de Control tuvieron este evento, pero la diferencia no fue estadísticamente significativa (P=0,21). </p>
                        <p>-El nivel de FEVI a nivel basal fue comparable entre los grupos, pero a los 6 meses el descenso fue mayor en el grupo de Control (62,25 ± 5,409 versus 56,15 ± 4,79; P<0,001). </p>
                        <p>-Con respecto a los biomarcadores cardíacos se observó un incremento significativo en la Mediana del nivel de TropI en el grupo con Placebo a los 6 meses (P=0,035). </p>
                        <p>-De manera similar, se observó un incremento significativo en el nivel de Pro-BNP en el grupo de Control, comparado con el grupo con Intervención a los 6 meses (49,60 ± 35,97 vs 98,60 ± 54,24).</p>
                        <p>-No se observaron diferencias significativas en la Mediana de los niveles de CK-MB entre los grupos, a los 6 meses (P=0,079).
                        </p>
                    </div>
                            
                    <div class="single-subsection">
                        <div class="uppercase-subtitle">Conclusión</div>
                        <p>El Tratamiento con enalapril tendría un rol para reducir la Toxicidad cardíaca en el corto plazo luego de la administración de antraciclinas.</p>
                    </div>
                            
