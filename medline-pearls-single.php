                    <div class="single-subsection">
                        <div class="uppercase-subtitle">Comentarios</div>
                        <p>Los autores comentan que a pesar que las antraciclinas se utilizan hace más de cinco décadas su mecanismo de cardiotoxicidad no está del todo aclarado. Una Hipótesis es la generación de radicales libres que llevarían a la peroxidacion de lípidos y daño a las membranas celulares. La Toxicidad cardíaca puede ser precoz o tardía (siendo esta última la que se presenta después de 1 año de Tratamiento con un curso crónico y progresivo). </p>
                        <p>Entre las fortalezas de este estudio los autores comentan su diseño de ensayo aleatorizado y controlado con Placebo, pero como limitación se menciona el Seguimiento relativamente corto. Por lo tanto se desconoce si enalapril tendría un efecto cardioprotector en el largo plazo.</p>
