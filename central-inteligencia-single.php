                   <p>Perinatal asphyxia remains a principal cause of infant mortality and long-term neurological morbidity, particularly in low-resource countries. No neuroprotective interventions are currently available. Melatonin (MLT), a potent antioxidant, anti-inflammatory and antiapoptotic agent, offers promise as an intravenous (IV) or transdermal therapy to protect the brain. We aimed to determine the effect of melatonin (IV or transdermal patch) on neuropathology in a lamb model of perinatal asphyxia. Asphyxia was induced in newborn lambs via umbilical cord occlusion at birth. Animals were randomly allocated to melatonin commencing 30 minutes after birth (60 mg in 24 hours; IV or transdermal patch). Brain magnetic resonance spectroscopy (MRS) was undertaken at 12 and 72 hours. Animals (control n = 9; control+MLT n = 6; asphyxia n = 16; asphyxia+MLT [IV n = 14; patch n = 4]) were euthanised at 72 hours, and cerebrospinal fluid (CSF) and brains were collected for analysis. Asphyxia resulted in severe acidosis (pH 6.9 ± 0.0; lactate 9 ± 2 mmol/L) and altered determinants of encephalopathy. MRS lactate:N-acetyl aspartate ratio was 2.5-fold higher in asphyxia lambs compared with controls at 12 hours and 3-fold higher at 72 hours (P < .05). Melatonin prevented this rise (3.5-fold reduced vs asphyxia; P = .02). Asphyxia significantly increased brain white and grey matter apoptotic cell death (activated caspase-3), lipid peroxidation (4HNE) and neuroinflammation (IBA-1). These changes were significantly mitigated by both IV and patch melatonin. Systemic or transdermal neonatal melatonin administration significantly reduces the neuropathology and encephalopathy signs associated with perinatal asphyxia. A simple melatonin patch, administered soon after birth, may improve outcome in infants affected by asphyxia, especially in low-resource settings.</p>
                   
