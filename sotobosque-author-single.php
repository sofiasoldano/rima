                    <div class="title">Ana María Matute</div>
                    <p>Ana María Matute, incorporada recientemente como miembro de la Real Academia Española, nació en Barcelona en 1926 de padre catalán y madre castellana. No ha realizado estudios superiores, por lo que su formación es profundamente autodidacta. </p>
                    <p>De precoz vocación literaria, con apenas 22 años hace su aparición con la novela Los Abel (1948), que queda finalista del premio Nadal. La obtención en 1959 de este importante premio con su obra Primera Memoria le supone la consagración definitiva. 
                    Antes, en 1952, ganaría el premio Gijón con Fiesta del Noroeste, el Premio Planeta en 1954 con Pequeño Teatro y el Premio de la Crítica (1958) y Nacional de Literatura (1959) con Los hijos muertos.</p> 
                    <p>Sigue la trilogía Los mercaderes formada por la ya mencionada Primera Memoria, Los soldados lloran de noche ,de 1964 (Premio Fastenrath en 1969) y La trampa (1969). </p>
                    <p>Su última novela, Olvidado Rey Gudú (1997), cierra una producción extensa y valiosa. 
                    Los niños tontos (1956), Historias de la Artámila (1961), El tiempo (1963) y Algunos muchachos (1968) son títulos destacados de su copiosa producción cuentística. </p>
                    <p>Es miembro de la Hispanic Society of America. Ha dictado cursos en la Universidad de Indiana y de Oklahoma. 
                    Sus obras han sido traducidas a diversos idiomas. </p>
                    <p>Dos experiencias influyen y condicionan gran parte de la producción de Ana María Matute: el Campo de Castilla y sus gentes, y la Guerra Civil como recuerdo y en sus consecuencias. El aislamiento de los pueblos perdidos por los caminos de España, la soledad social, la infructuosidad de todo esfuerzo por salir de una situación de empobrecimiento, aparecen retratados en la producción de Ana María Matute, revelando su preocupación social que, en algunos casos, asume matices de franca denuncia. </p>
                    <p>Pero no sólo refleja su obra la situación de soledad y pobreza de quienes habitan el campo; también vuelve su mirada hacia la burguesía adinerada que, encerrada en sus cómodas redes, se muestra cruel y egoísta, aunque también sometida a situaciones de conflicto entre el ocio y la riqueza, o la enajenación progresiva. 
                    </p>
                    <p>Un lugar especial ocupan los niños dentro de la narrativa de Ana María Matute. Sus niños generalmente están solos: los de los pueblos, privados de su infancia al ser víctimas de la violencia, del abuso autoritario, y los burgueses, aislados del mundo en su material bienestar. 
                    Si bien en su producción prima el estilo realista, también encontramos relatos en los que se inclina hacia un mayor lirismo. </p>

                    <p>El árbol de oro integra el volumen Historias de la Artámila, editado por Destino.</p>
