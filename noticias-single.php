                <div class="content">
                    <div class="title">La aparición de la fibrilación auricular estaría asociada con la presión arterial.</div>
                    <p>La presión arterial sistólica diurna y la nocturna fueron predictores significativos de la fibrilación auricular incidente. La carga de presión arterial sistólica durante el día de más del 38% se asoció con un aumento significativo del riesgo de padecer la aparición de fibrilación auricular. </p>

                    <p>Así lo sugiere el estudio “Ambulatory blood pressure and long-term risk for atrial fibrillation” (Heart. Vol: 104 Nro: 15 Págs: 1263 - 1270 Fecha: 01/08/2018). </p>

                    <p>Los datos sobre la contribución de los componentes de la presión arterial ambulatoria (PAP) para el  riesgo de desarrollar fibrilación auricular (FA) son limitados. Por eso, los investigadores intentaron probar, a través de un estudio prospectivo, la hipótesis de que la PAP puede ser de utilidad para estimar el riesgo de desarrollar FA en un estudio poblacional europeo.</p>
                    
                    <div class="highlighted blue">
                        <p>Con ese objetivo, se registró la presión arterial diurna (PA) en 3.956 pacientes reclutados aleatoriamente de la población general en cinco países europeos. De estos participantes, 2.776 (70.2%) se sometieron a una monitorización completa de PAP de 24 horas. La mediana de seguimiento fue de 14 años. </p>
                    </div>
                    
                    <p>La carga de PA sistólica diurna se definió como el porcentaje de las lecturas de la PA por encima de 135 mm Hg. La incidencia de FA se evaluó a partir de los ECG obtenidos al inicio del estudio y el seguimiento y de los registros en poder de médicos generales y / o hospitales.</p>

                    <p>Durante los 58.810 años-persona de seguimiento, 143 participantes experimentaron FA de nueva aparición. En los modelos ajustados de Cox, cada aumento de 1 desvío estándard (DE) en el valor basal de 24 horas, la PA sistólica diurna y nocturna se asoció con un aumento del 27% (P = 0,0056), 22% (P = 0,023) y 20% (P = 0,029) en el riesgo de AF incidente, respectivamente. </p>

                    <p>La PA sistólica convencional estaba en el límite con el riesgo de FA (18%, p = 0,06). En comparación con el riesgo poblacional promedio, los participantes en el cuartil inferior de la carga sistólica de PA durante el día (<3%) tuvieron un 51% (P = 0.0038) menor riesgo de FA incidente, mientras que en el cuartil superior (>38%), el riesgo fue un 46% mayor (p = 0,0094).</p>
