                    <div class="title max">Cardiotoxicidad inducida por antraciclinas en pediatría: <br>¿Enalapril tendría un efecto preventivo?</div>
                    <div class="caption">
                        <p>Rol de los inhibidores de la enzima convertidora de angiotensina en la cardiotoxicidad inducida por antraciclinas: ensayo aleatorizado, en doble ciego, y controlado con placebo.</p>
                    </div>
                    <div class="caption">
                        <p class="caption-subtitle">Artículo original:</p>
                        <p>Role of ACE inhibitors in anthracycline-induced cardiotoxicity: A randomized, double-blind, placebo-controlled trial.</p>
                    </div>
                   <div class="caption borders">
                        <p>Vineeta Gupta, Sunil Kumar Singh y colaboradores.</p>
                        <p>Pediatr Blood Cancer. 2018 Nov;65(11):e27308. </p>
                    </div>
                    <div class="single-subsection">
                        <div class="uppercase-subtitle">Introducción</div>
                        <p>Gracias a las nuevas estrategias terapéuticas actualmente aumentó de manera significativa la Tasa de sobrevida en niños con cáncer, llegando hasta el 80% a 5 años. Sin embargo, la mejoría de la sobrevida se encuentra asociada a varios efectos adversos generados por los tratamientos, que se manifiestan en varios años, siendo uno de ellos la cardiotoxicidad, principalmente atribuida a las antraciclinas e irradiación mediastínica. Las antraciclinas forman parte de casi el 50% de los protocolos de tratamientos oncológicos pediátricos. </p>
                        <p>La cardiotoxicidad inducida de este grupo de fármacos es Variable y puede presentarse con un rango desde alteraciones electrocardiográficas asintomáticas hasta miocardiopatía clínicamente manifiesta. La detección, prevención y Tratamiento precoz de la Toxicidad cardíaca es un área de permanente investigación y en los últimos años se demostró cierto efecto beneficioso con la utilización de enalapril; pero la evidencia es de baja Calidad metodológica. </p>
                        <p>El objetivo de este estudio fue evaluar el rol del enalapril respecto a la cardiotoxicidad inducida por antraciclinas, evaluada a través de la fracción de eyección del ventrículo izquierdo (FEVI) y a través de biomarcadores cardíacos, incluyendo troponina I (TropI), pro-péptido natriurético cerebral (Pro-BNP, por sus siglas en inglés) y creatinaquinasa MB (CK-MB), con la Hipótesis de que este fármaco tendría cierto rol cardioprotector.</p>
