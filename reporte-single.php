                   <p>In a recent edition of this journal, Mannion and Braithwaite provide a succinct analysis of the emergence, and ultimately limited impact, of what they term the current ‘Safety I’ movement in healthcare. They describe the arc of this field from denial, through engagement via mechanisms and approaches imported from other industries, to the current situation where, despite ‘best efforts,’ error rates remain stubbornly recalcitrant. In examining the failure of system-wide efforts to produce sustained reductions in errors and adverse events, that article exposes the doxa, or what Bourdieu calls ‘the taken for granted’ which is central to this latest wave of patient safety movement. In this commentary, I would like to take focus on two key elements of Mannion and Braithwaite’s argument: that harm is caused by misguided but otherwise well-intentioned actions and the ‘embracing’ of patient safety. </p>
